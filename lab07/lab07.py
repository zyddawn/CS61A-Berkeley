def store_digits(n):
    """Stores the digits of a positive number n in a linked list.

    >>> s = store_digits(1)
    >>> s
    Link(1)
    >>> store_digits(2345)
    Link(2, Link(3, Link(4, Link(5))))
    >>> store_digits(876)
    Link(8, Link(7, Link(6)))
    """
    "*** YOUR CODE HERE ***"
    cur_d, n = n%10, n//10
    lnk = Link(cur_d)
    while n > 0:
        cur_d, n = n%10, n//10
        lnk = Link(cur_d, lnk)
    return lnk


